using Avalonia;
using ExifGlass.Core.Models;
using ExifGlass.Integration;

namespace ExifGlass;

internal class Program
{
    // Initialization code. Don't use any Avalonia, third-party APIs or any
    // SynchronizationContext-reliant code before AppMain is called: things aren't initialized
    // yet and stuff might break.
    [STAThread]
    public static void Main(string[] args)
    {
        // Windows-only integration: add the ImageGlass 9 external-tool host to the shared seam.
        // Every other mode (standalone, ImageGlass 10) falls through to the library's default.
        App.SourceHostFactory = static (options, _) => options.Mode == AppMode.ImageGlass9
            ? new ImageGlass9SourceHost(options.InitialFilePath)
            : null;

        BuildAvaloniaApp().StartWithClassicDesktopLifetime(args);
    }

    // Avalonia configuration, don't remove; also used by visual designer.
    public static AppBuilder BuildAvaloniaApp()
        => AppBuilder.Configure<App>()
            .UsePlatformDetect()
#if DEBUG
            .WithDeveloperTools()
#endif
            .WithInterFont()
            .LogToTrace();
}
